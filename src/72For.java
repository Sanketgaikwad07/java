import java.util.Scanner;
class For {
    public static void main(String[] args) {
        Scanner sc =new Scanner(System.in);
        int sum=sc.nextInt();

        for(int i = 0; i<sum; i++){
            for(int j=0;j<sum;j++){
                System.out.println("Saket");
            }
        }

    }
}
