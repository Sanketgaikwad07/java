
import java.util.Scanner;

class Solutio {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int sum = 0;
        for (int i = 0; i < a; i++) {
            int num = sc.nextInt();
            sum += num;
        }
        System.out.println(sum);
    }
}
