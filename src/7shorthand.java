import java.util.Scanner;

 class shorthand {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int a=5;
        int x1=sc.nextInt();
        a=a+x1;
        System.out.println(a);
        int x2=sc.nextInt();
a=a+x2;
        System.out.println(a);
        int x3=sc.nextInt();
        a=a*x3;
        System.out.println(a);
        a+=1;
        System.out.println(a);


    }
}
